w = [".-", "-...", "-.-.", "-..", ".", "..-.", "--.", "....", "..", ".---", "-.-", ".-..", "--", "-.", "---", ".--.",
     "--.-", ".-.", "...", "-", "..-", "...-", ".--", "-..-", "-.--", "--.."]

for t in range(int(input())):
    s = input().split()
    print(f"Case {t+1}: ", end="")
    for i in s:
        print(chr(65 + w.index(i)), end="")
    print()
