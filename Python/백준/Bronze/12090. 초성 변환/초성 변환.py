import re

s = input()
p = [("[가-깋]", "ㄱ"), ("[까-낗]", "ㄲ"), ("[나-닣]", "ㄴ"), ("[다-딯]", "ㄷ"), ("[따-띻]", "ㄸ"), ("[라-맇]", "ㄹ"),
     ("[마-밓]", "ㅁ"), ("[바-빟]", "ㅂ"), ("[빠-삫]", "ㅃ"), ("[사-싷]", "ㅅ"), ("[싸-앃]", "ㅆ"), ("[아-잏]", "ㅇ"),
     ("[자-짛]", "ㅈ"), ("[짜-찧]", "ㅉ"), ("[차-칳]", "ㅊ"), ("[카-킿]", "ㅋ"), ("[타-팋]", "ㅌ"), ("[파-핗]", "ㅍ"),
     ("[하-힣]", "ㅎ")]

for i in p:
    s = re.sub(i[0], i[1], s)
print(s)
